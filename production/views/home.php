(function() {
var africa = new google.maps.LatLng(11.436955,16.772459);
var opciones = {
zoom : 4,
center: africa,
 scrollwheel: false,
mapTypeId: google.maps.MapTypeId.ROADMAP
};
var div = document.getElementById('map');
var map = new google.maps.Map(div, opciones);
var marcadores = [],
visibles = ['Technology','Clouthing','Meals'];
var image = {
url: 'assets/img/etiquetas.png',
size: new google.maps.Size(68, 72)
};
var insertar_marcador = function (data){
$.each(data, function(i, obj){
var marcador = new google.maps.Marker({
  title: obj.name,
  position : new google.maps.LatLng(obj.coords.lat, obj.coords.lng),
  map : map,
  tipo : obj.type,
  icon : image

});
google.maps.event.addDomListener(marcador, 'click', function(){
   $('.fondo-pop-up').fadeIn(300);
   $('.cuerpo-pop-up').addClass('zoomIn').fadeIn();
   setTimeout( function(){ $(".cuerpo-pop-up").removeClass('zoomIn')} ,1000);
});
google.maps.event.addListener(marcador, 'mouseover', function(){
    this.setIcon('assets/img/etiquetas-hover.png');
});
google.maps.event.addListener(marcador, 'mouseout', function(){
    this.setIcon('assets/img/etiquetas.png');
});
marcadores.push(marcador);
});
};
var ocultar_marcadores = function(){
for (var i = 0, length = marcadores.length; i < length; i++){
marcadores[i].setVisible(visibles.indexOf(marcadores[i].tipo) !== -1);
}
};
$.getJSON('partials/technology.json',insertar_marcador);
$.getJSON('partials/clouthing.json',insertar_marcador);
$.getJSON('partials/meals.json',insertar_marcador);
$('input.control').on('change',function(e){
var $this = $(this),
valor = $this.val();

if ($this.is(':checked')){
visibles.push(valor);
}
else {
visibles.splice(visibles.indexOf(valor), 1);
}
ocultar_marcadores();
});
})
();

</script>

