function readRss() {
   $xml = ("http://allafrica.com/tools/headlines/rdf/business/headlines.rdf");
   $xmlDoc = new DOMDocument();
   $xmlDoc->load($xml);

   $objXML = simplexml_load_file($xml);

   $i = 0;
   foreach($objXML->channel->item as $var){

    echo '<div class="caja-comentario-indv">
                 <a href="' . $var->link . '" target="_black">
                   <h3 class="grey">' . $var->title . '</h3>
                 </a>
                 <p>' . $var->description . '</p>
                 <span> <i class="fa fa-calendar-o"></i> ' . $var->pubDate . '</span>
                 <a href="' . $var->link . '" target="_blank" class="grey"><i class="fa fa-arrow-circle-right"></i> More</a>
                 <div class="clear"></div>
           </div>';
    $i++;
    if($i > 1) break;

   }

}
